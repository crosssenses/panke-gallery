                     <p><strong><a href="http://www.sebastianschmieg.com">Sebastian Schmieg</a> </strong>examines the ways networked technologies shape online
and offline realities, in artworks that range from shredded hard-drives
from a Google datacenter to crowd-sourced versions of popular self-help
books using Amazon's Kindle. His output encompasses websites, interface
performances, algorithmic videos, online interventions, print-on-demand
books or neural networks. He focuses on a critical engagement with the
manifold and ubiquitous nodes of the Internet – both human and
technological – as sites of the political. Previously his work has been
exhibited at Transmediale, Berlin, Germany; Art Center Nabi, Seoul,
South Korea; Bitforms Gallery, New York, USA; and The Photographers’
Gallery, London, UK. Sebastian Schmieg lives and works in Berlin.</p>

<p><strong><a href="http://silviolorusso.com">Silvio Lorusso</a> </strong> is a Rotterdam-based artist, designer, and researcher. He is currently busy with the investigation of the relationships between entrepreneurship and precarity, i.e. entreprecariat. His work was presented at Transmediale (DE), Vögele Kultur Zentrum (CH), NRW-Forum (DE), MoneyLab (NL), Impakt (NL), Sight & Sound (CA), Adhocracy (GR), Biennale Architettura (IT). He holds a Ph.D. in Design Sciences from the School of Doctorate Studies – Iuav University of Venice. His writing has appeared in the Immaterial Labour Union Zine, Prismo, Printed Web 3, Metropolis M, Progetto Grafico, Digicult, Diid, and Doppiozero. Currently, he is an affiliated researcher at the Institute of Network Cultures of Amsterdam and he works as a mentor at the Amsterdam University of Applied Sciences’ PublishingLab.</p>
