                    <p> Mit der Einf&uuml;hrung des Computerspiels entwickelte sich eine eigene Kultur des elektronischen Spiels. Die Veranstaltungsreihe will Arbeiten von in Berlin lebenden Künstler*innen vorstellen, die sich kritisch mit den Computerspielen und ihren sozial-politischen Wirkungen auseinandersetzen. Parallel widmet sich das Filmprogramm in einer Reihe von Dokumentar- und Kunstfilmen dem E-Sport, der als Spezialfall des Spiels als Spiegel und Exerzierfeld der digitalen Netzwerkgesellschaft betrachtet wird.</p>
