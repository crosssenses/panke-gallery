                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p>Looking at the qualities and characteristics of net art the need for an expanded practice of conservation becomes clear. Most net artworks are build and distributed through a complex and intricate interrelated system of networks that presents an assemblage of art, technology, politics, and social relations – all merged and related to form a variable entity. Adding to these qualities are the quick obsolescence of hard- and software; the fact that knowledge and expertise is distributed through communities; that many net artworks are created within restricted platforms or are based on open source ideologies; lastly, that net artworks often act as assemblages that change or mutate, make net art a challenging case for conservation.</p>
