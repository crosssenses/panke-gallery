                    <a href="http://aaaan.net/"><strong>Annet Dekker</strong></a> is an independent researcher/curator. Currently she is Assistant Professor Archival Science at the University of Amsterdam and Visiting Lecturer at London South Bank University. Previously she was Researcher Digital Preservation at Tate, London, tutor at Piet Zwart Institute, Rotterdam, and fellow at Het Nieuwe Instituut, Rotterdam. She publishes widely on issues of digital art and preservation in international, peer-reviewed journals, books, and magazines, and has edited several publications, among others, Speculative Scenarios, or what will happen to digital art in the (near) future (2013), Archive 2020: Sustainable Archiving of Born Digital Cultural Content (2010), and Walled Garden (co-edited with Annette Wolfsberger, 2009). She worked as web curator for SKOR (2010–12), was programme manager at Virtueel Platform (2008–10), and head of exhibitions, education and artists-in-residence at the Netherlands Media Art Institute (1999–2008). In 2014, she completed her PhD on a conservation of net art at Goldsmiths University of London.</p>
