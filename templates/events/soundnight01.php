	                <p><a href="http://www.i-a-m.tk/r-c-o.html "><strong>RCO</strong></a> is an artist collective that wires up everyday objects and that develops numerous circuitry to generate and experiment in sounds and noises. A variety of devices and noise sources are arranged in interactive feedback-loops and polyphonic sound installations.
Live RCO assembles momentary and improvised setups that consider specifics of the particular space and its situation and integrates these into the continuously changing acoustical environment.
