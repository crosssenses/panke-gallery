<p><br><a href="http://nadjabuttendorf.com/"><strong>Nadja Buttendorf</strong></a>  presents her current work:<br><em> "The Adventures of WH - Kapitel 1: Diskriminator"</em>, which deals with socialist microchip design, archive handling and personal competition within the post gender micro electronic universe. In collaboration with Anne Baumann, they look at Buttendorf's step grandfather and physicists Werner Hartmann (1912-1988), who is known to be the founder of microelectronics in GDR.</p>
