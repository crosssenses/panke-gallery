                <p><br> <a href="http://houseofkilling.net/"><strong>Esben Holk</strong></a><br>HOUSE OF KILLING and how to absorb the ontological uncertainty of Jennifer Aniston
come with anarchist art collective and co-creator of Jennifer Aniston Superfans: HOUSE OF KILLING on a philosophical inquiry on what Jennifer Aniston and other referential data can bring to the production of the posthuman experience, and consequently: the increasingly irrelevant art of Esben Holk. <br>
HOUSE OF KILLING is a house that kills. It is an undefined flexible anarchist queer collective, put into this world to kill ideas, structures, dreams and realities, in order to promote a generation of undefined flexible queer youth able and willing to do whatever the fuck they want.<br>
HOUSE OF KILLING feat Esben Holk is an avid online entity: a posthuman queer artist working in browserbased experiences, performative strategies and creative coding. Concerned with the absence of ontological density and anti-essentialist epistemology in current ideology, he approaches the agility of proposed productions of enforced entities, like: the queer, the citizen and the worker. Vehemently xenofeminist and anti naturalist, the artist manifests work as part of a grand online performance celebrating the internet, and its ability to subvert and overthrow the established world order.<br><br>
​Didnt I tell u, I was lost? infinitely shared and used in corners of virtual spaces I dont know about, in crevices not even the dankest of users would dare go into. Unsurprisingly, the work left me behind: now its clickbait on my website.<br><br>
​
I JUST WANTED U TO KNOW, that baby, WE R LOST<br>
xoxo<br>
Esben Holk
