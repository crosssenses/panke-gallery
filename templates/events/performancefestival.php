	                <p><a href="https://www.max-grau.de"><strong>Max Grau</strong> </a>will present his work and his upcoming live streaming project <em>[<s>workingtitle</s>][sudo keylogger /Users/machs/Büro/Ausstellungen/2018/2018_Panke/log.txt]</em> hosted by panke.gallery <br><br>
<a href="https://www.max-grau.de">Max Grau</a> (*1988) is a visual artist and writer based in Berlin. He studied Fine Arts in Saarbrücken, Berlin and Los Angeles and uses a variety of media such as video, text, email, performance, photography, sound and printed matter. Besides doing things individually he’s interested in friendship-based models of collaboration and forms a part-time duo with Jan Erbelding. Since 2016 he has been involved in *foundationClass – an educational project located at Kunsthochschule Weißensee that tries to support artists who had to flee their home countries in the process of gaining access to the German art school system. His work has been shown internationally, for example at Galerie La Croix Los Angeles, Kunstmuseum Wolfsburg, Galerie Lily Robert Paris, Pet Projects Perth and Digital Art Centre Taipei. His first book ›You See I’ve Always Wanted Things To Be Beautiful‹ was published in 2018 by Ruine München/Hamman van Mier Verlag.
