<br><strong>Workshop:</strong> <em>#HotPhones - high-tech self-care</em><br>
	In this workshop you learn how to get your phone hotter than you, because a #hotphone helps to release and relax your tightened muscles. The #hotphone massage is a relaxing and deeply soothing form of massage. It's a helpful tool you can easily integrate in your daily life. It makes you fit again and activates new energy for you and your smartphone. (EN)</p>

