	                <p><a href="https://ww.virtual-slap.net"><strong>Wilted Woman</strong></a> <br>Noise / industrial concrète of two weapons groan sound features a techno-Buripu, graveyard soundscapes end values and mysterious mood has spread! <br>...IN OTHER WORDS: sound-artist working in the field of electronic music. Aliases include DJ Puddle, ssslemo, burning eel, variations on registered legal name, but primarily Wilted Woman. Recent sounds released on Primitive Languages, Plastic Bags, Alien Jams, and many others.</p>

	                <br>

	                 <div class="image-wrapper.fit-height">
                        	<img src="/img/soundnight/soundnight03/moma.jpg" />
                    	</div>

	                <p><a href="https://www.youtube.com/user/musicofmodernart"><strong>MOMA</strong></a> <br>was founded 2010 after they played in an other Art band called BAUT by the two Berlin based contemporary artists and brothers Theo Boettger (Paintings and Installation) and Martin Böttger as well known as TSAWORKS (sculpture and media art).<br>
Their sound is a mixture of experimental noise and hysterical, chopped superimposed melodies that form to soundtrack-like pieces.
Constrained by dark and glitchy rhythms filled up with flickering pop structures, spoken word, grime , post - punk elements.
Bass heavy synthesizer sequences are merging with energetic e-guitar compositions into dynamic songs which are mirroring a dystopian future.
Moma’s performances are arranged live in combination with self produced video projections that emphasize its contemporary relevance to music and video.
They played shows at Gallery's and Art institutions such as Galerie3ringe Leipzig / Kunstsammlung Gera / Staatliche Kunstsammlung Dresden / Kunstverein Wolfsburg
Urban Shaman Gallery Winnipeg (Cluster festival ) / Shiny Toys festival Mühlheim ander Ruhr  / Art- Spring Berlin / Wabe Berlin / Club Josef Berlin.</p>

					   <br>

						<div class="image-wrapper.fit-height">
                        	<img src="/img/soundnight/soundnight03/sparkasse_wedding.jpg" />
                    	</div>

                   <p><a href="http://www.sparkasse.wedding"><strong>sparkasse.wedding</strong></a> was founded 2015 in Berlin by the artists due Kim Asendorf and Ole Fach. They have been in the studio for two years and are now doing their first show at Panke.Gallery. SKW is basically a producers only due with a background in digital art and long history in clubbing. Without ever having published a song yet they have several requests from all over Berlin.</p>

						<br>

				   <div class="image-wrapper.fit-height">
                        	<img src="/img/soundnight/soundnight03/landlord-24-31.png" />
                    	</div>

	                <p>Hailing from Berlin <a href="http://diy-church.blogspot.de/"><strong>DJ SchluchT</strong></a> works at DIYChurch.org as one of their unpaid interns since more than 8 years. He made himself a name with bad social behaviour online and in real live as well as naive ghost coding and tape looping. His Dj sets are very eclectic – drug referential stoner-logic reflecting on movies without watching them and basically it always sounds the same or like one of his critics once said: classic intuitive wrongfootedness. When he is not djing or radioing, shlucht likes to sit in his virtual bay & watch his yachts, little known but he runs a virtual yacht club where he shares his enthusiasm for underwater landscape painting and networked waves.
He also released some mixtapes & audio works :<br>
<a href="http://zamzamrec.bandcamp.com/album/snakes-on-a-plane"><strong>Snakes on a Plane</strong></a> via Bristols ZAMZAM records <br>

<a href="https://stenzequo.bandcamp.com/album/dj-shlucht-dark-films-that-eyes-see-at-night"><strong>Dark films that eyes see at night</strong></a> via Stenze Quo records <br>

<a href="https://morerecords.bandcamp.com/album/spears-utilitarian-outlook"><strong>spears – Utilitarian Outlook</strong></a> via more records </p>

