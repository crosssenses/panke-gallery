	                <p><a href="https://www.facebook.com/neitherfamousnorrich"><strong>NFNR (Neither Famous Nor Rich)</strong></a> <br>is a nickname for Kyiv-based musician, theatrical composer and event curator from Ukraine - Olesia Onykiienko. She also belongs to musicians community Teple Yabluko, specializing on multichannel space performances. NFNR has joint projects with different musicians from Ukraine, Sweden, India, Japan and is a part of experimental folk-electronic art-laboratory MOKOSHO. As well she contributes as audial part in a duo with artist Kateryna Berlova, with who they create art-interventions and installations.   <br>
	                By combining the multilayered sound textures created from sounds collected and processed together with generated with different types of synthesis NFNR bases on them partly improvisational live sets, that remind `electronic pictures` and sound-collages. This time new non-meditative show from upcoming album Mydriasis will be represented.
