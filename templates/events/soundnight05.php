	                <p><a href="http://www.extralight.de/?p=388"><strong>Ran Ancor - 空 Ku, the Void - Version</strong></a> <br> Kū, the Void is a synesthetic, live-generated audiovisual experience that combines hacked scientific technology and custom-developed software to expressively explore the ephemeral/immaterial. Rich, evolving oscilloscope visuals (Ran Ancor) are synchronized via computer vision (Abe Pazos) with a lush, responsively synthesized soundscape (Mei-Fang Liau). In this hybrid system, sensitively fluctuating analog signals, fine-tuned data analysis, and human intuition perform together as one coherent living being.</p>


