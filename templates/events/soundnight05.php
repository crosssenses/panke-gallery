	                <p><a href="http://kohlberger.net"><strong>Rainer Kohlberger</strong></a> <br> is an Austrian born freelance visual artist / film maker living in Berlin. His work is primarily based on algorithmic compositions with reductionistic aesthetics influenced by flatness, drones and interference. Within his works there always lies a layer of noise, that fascinates him as a sense of the infinite, which is both the ultimate abstraction and inveterately fuzzy. In his films, installations and live performances maximum forms of intensities come into play. His work has won several prizes internationally.<br>
<strong>brainbows</strong> Kohlberger uses digital-projection technology in a radical way. Through impulses and waves of pure light, the intentional overload of the human perception apparatus leads to visual impressions that appear exclusively in the literal »eye of the beholder«. Layers of noise, drones and stroboscopic lights unfold. Image surface and image space meld into one (consciousness-)state, in which categories such as being and non-being, material and immaterial appear obsolete.
</p>
