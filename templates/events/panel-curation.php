                    <h3>curating as a network technology </h3>
                </div>

                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p>This panel hopes to address questions including: how much can curating can be understood as a network technology that trades in social capital? What importance should be ascribed to the early experiments in net-based curating within the context of current digital curating practices? What is the relationship of "Netzkunstkultur" to the art and gallery system? To what extent has so-called social media changed what it means to curate? Could curating be, for example, democratized? Did something like self-branding already exist in the 90s and what role does it play now for artists and curators? Has net art's dream of liberating production and distribution from the institutions been realized? Can purely online exhibitions compete with the physical gallery system?  How can we deal with the normalized same, which is produced by the use of the so called "public curation tools" like playlists, (image) boards, (Tumblr) threads and (Facebook) friend lists …</p>
                    <p style="font-weight: 300;">&mdash; The panel takes place in the context of the exhibition <a href="<?php get_url('exhibition','transmediale'); ?>"><strong>1990s Berlin Net Art -- Towards a Critical Evaluation</strong></a></p>
