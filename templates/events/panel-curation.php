                <div class="col-md-8 col-sm-8 col-xs-12">

	                <p><a href="http://www.karenarchey.com"><strong>Karen Archey</strong></a> is an art critic and independent curator based in Berlin. She is editor of e-flux conversations, and a 2016 Creative Capital | Warhol Foundation Arts Writers Grant recipient for short-form writing. With a focus on feminist practices, art and technology, her writing is regularly featured in magazines such as frieze and ArtReview, and anthologies published by institutions such as the Whitney Museum of American Art, MIT Press and New Museum. Archey recently co-curated the survey exhibition Art Post-Internet at Ullens Center for Contemporary Art in Beijing (2014) and edited the publication Art Post-Internet: INFORMATION/DATA. A regular public speaker, she has recently spoken at Renaissance Society at University of Chicago, Institute of Contemporary Arts London, Museum of Modern Art New York, MoMA PS1, and elsewhere.</p>

	                <p><a href="http://fuenfnullzwei.de"><strong>Holger Friese</strong></a> (*1968, lives and works in Bernau bei Berlin) is a visual artist and graphic designer. In the mid to late nineties, he gained attention with his Internet-based artworks and developed and exhibited his work at documenta X, SFMOMA, and ZKM in Karlsruhe among others. In both his artistic and professional work he is interested in collaborating with other experts to expand ideas further than is possible alone. In addition to his everyday work, he has taught media design and media art at various institutions and has lectured in Germany, Europe, and North America. Since 2015 he acts as the artistic director for the international summer school at the Bauhaus Denkmal Bundeschule Bernau..</p>

                   <p><a href="http://www.sujaschko.de"><strong>Dr Susanne Jaschko</strong></a> is an independent curator of contemporary art and based in Berlin, Germany. From 1997 to 2004, Jaschko was curator at transmediale. In 2008/09, she was Head of Presentation and of the Artist in Residence Programme at the Netherlands Media Art Institute in Amsterdam.<br>
Among the exhibition projects she curated are: Colomboscope Testing Grounds, Art and Digital Culture in South Asia and Europe, Colombo, Sri Lanka, 2016; Process as Paradigm – Art in Development, Flux and Change, LABoral Gijon, 2010; Visual Voltage Amplified, Felleshus, Berlin, 2010; Travelling Without Moving, Oboro, Montréal, 2007; urban interface berlin/oslo, 2007; Urban Screens Manchester 07; Open House, Vitra Design Museum/Art Center Pasadena a.o. 2006 – 2008; Scape, Don‘t Misbehave!, Biennial of Art in Public Space, Christchurch, New Zealand, 2006.
She has taught at academic institutions in Germany and abroad – such as Bauhaus University Weimar, Oslo National Academy of the Arts, University of Applied Sciences Potsdam and Berlin University of the Arts.</p>

