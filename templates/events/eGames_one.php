        <section class="further">  <!-- Artists -->
            <div class="row">
                <div class="col-md-4 col-sm-4 col-xs-12">
                    <h3>Artists</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">

	                <div class="image-wrapper 3-col">
                        	<a data-flickr-embed="true"  href="https://www.flickr.com/photos/bartholl/15138009348/in/album-72157647904042972/" title="Hurt me plenty"><img src="https://farm6.staticflickr.com/5564/15138009348_b80e026d51_k.jpg" width="2048" height="1365" alt="Hurt me plenty"></a><script async src="//embedr.flickr.com/assets/client-code.js" charset="utf-8"></script>
                    	</div>

	                <p><a href="https://arambartholl.com"><strong>Aram Bartholl</strong></a> spricht in seinem Vortrag über den Einfluss von
Computerspielen auf seine k&uuml;nstlerische Praxis und wird einige Projekte aus diesem Zusammenhang genauer erl&auml;utern.</p>
					<p>Aram Bartholls Werk bewegt sich an der Schnittstelle zwischen Internet, Kultur und Realit&auml;t. Die vielf&auml;ltigen Kommunikationskan&auml;le sind selbstverst&auml;ndlich geworden, doch wie beeinflussen uns diese? Gem&auml;ß des Paradigmenwechsels der Medienforschung fragt Bartholl nicht nur, was der Mensch mit den Medien macht, sondern auch in wie weit die Medien den Menschen ver&auml;ndern. Das Spannungsverh&auml;ltnis von öffentlich und privat, online und offline, von Technologieverliebtheit und Alltagsleben liegt im Kern seines Schaffens. In Form von Interventionen und Installationen im öffentlichen Raum untersucht Bartholl die Wirkung, wenn Bestandteile der digitalen Welt mit der Realit&auml;t zusammentreffen. Neben zahlreichen Vortr&auml;gen, Workshops und Performances wurden seine Arbeiten international u. a. ausgestellt im MoMA Museum of Modern Art, den Skulptur Projekten Münster und Venedig Biennale. Aram Bartholl lebt und arbeitet in Berlin.</p>
					<br>

						<div class="image-wrapper 3-col">
                        	<img src="https://media.mutualart.com/Images/2017_11/19/22/224530217/c95dc6df-f8cd-457b-84df-b42f719b7bbc.Jpeg" />
                    	</div>


	                <p>Im Kontext des frühen Internets und der Neuen Medien hat <a href="https://www.evagrubinger.com/"><strong>Eva Grubinger</strong></a> seit Mitte der 1990er Jahre das Medium der Skulptur fur sich weiterentwickelt und die Auseinandersetzung mit physischer R&uuml;umlichkeit gegen eine vordergründige Virtualisierung ins Zentrum ihrer Arbeit gerückt. Sie besch&uuml;ftigt sich auf unterschiedliche Weise mit den semantischen Potentialen von Objekten. Das Motiv des Spiels tritt dabei in Ihrem Werk wiederholt in Erscheinung. Nach einer kurzen Einführung besteht die M&ouml;glichkeit Grubingers Arbeit ‘Hype!, Hit!, Hack!, Hegemony!’ (1996) – 4 analoge Brettspiele, welche die Spieler zu Akteuren in den Feldern Kunst, Popmusik, Internet, und politische Theorie machen, auszuprobieren. </p>
<p>Eva Grubinger, geboren 1970 in Salzburg, lebt in Berlin. Ihre Arbeiten wurden an zahlreichen internationalen Orten gezeigt, u.a. mit Einzelausstellungen im Museum Abteiberg Mönchengladbach, dem Kiasma-Museum Helsinki, der Schirn Kunsthalle Frankfurt, oder dem ICA in London. Weiters hat sie an zahlreichen Gruppenausstellungen teilgenommen, u.a. in den Deichtorhallen Hamburg, dem Taipei Fine Art Museum, dem Krannert Art Museum, der Marrakech Biennale oder im Witte de With Center for Contemporary Art Rotterdam.</p>



