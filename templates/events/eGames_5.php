                    <p>"Does the world exist when I'm not watching it?" And: "Where does this world
end?" In Parallel II, Harun Farocki brings up some of the earliest and most
fundamental questions of gaming. We are going to propose a few bits of extra
material, in which non-filmmakers - gamers, rather - try to find concrete
answers to Farocki's questions, and discover a series of new problems: the
existence of other characters in the world, the distinction between "off the
map" and "under the map", and the issue of technical defects in the territory.</p>

	                <p><strong><a href="https://www.rolux.org">Sebastian L&uuml;tgert </a></strong>(a.k.a. Robert Luxemburg) lives and works in Berlin as an artist, writer and programmer. He is the co-founder of Bootlab (2000-2007), Pirate Cinema Berlin (2004-), 0xDB (2007-) and Pad.ma (2008-), and was the co-organizer of several international conferences, like The Dictionary of War (Frankfurt-Munich-Graz-Berlin 2006-2007), The Oil of the 21st Century (Berlin 2007), The Summit of Non-Aligned Initiatives in Education and Culture (Berlin 2007) and Properties of the Autonomous Archive (Bombay 2011). His work has been shown in numerous exhibitions, among them Animations (PS1 2001, Kunstwerke 2002), Werkleitz Biennale (2004), Poor Man's Expression (Kino Arsenal 2004, Artists Space 2012), Van Abbe Museum (2006), Manifesta (2008), Gwangju Biennale (2008), Taipei Biennale (2008), Artists Space (2012), Documenta (2012, with Pad.ma), Istanbul Biennale (2015) and Pane per poveri (2017).</p>
