                    	 <p><a href=""><strong>EMPIRE</strong></a> (2008­–2012) by Philip Solomon<br>
	                 "Phil Solomon’s “EMPIRE” (2008/2012), an installation made in the videogame space of Grand Theft Auto, raises issues of memory and materiality as they pertain to the medium specicities of film and digital media. As a remake of Andy Warhol’s Empire (1964), the work addresses concerns pertinent to structural  lm of the 1960s and 1970s, including, on the one hand, the material fragility of  lm as a physical medium, and, on the other, the immaterial endurance of an idea of cinema. “EMPIRE” expresses structural film’s contradictory approaches to materiality in digital terms by using a videogame object that is temporally ‘indefinite’, as indicated in the work’s accompanying wall text. Additionally, as part of the series In Memoriam (Mark LaPore), “EMPIRE” is a work of mourning. As such, its indefinite running time offers a meditation on the temporality of memorials and the capacity of film, photography and digital media to realize the process of memorialization." <a href="https://www.academia.edu/9321670/Cinema_Immemorial_EMPIRE_and_the_Experimental_Machinima_of_Phil_Solomon"> Genevieve Yue</a>  </p>

