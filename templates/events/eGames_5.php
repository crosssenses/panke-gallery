                </div>

                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p> The invention of video games created a new form of gaming culture. In the series "eGames", which runs from April to August, panke.gallery presents works from Berlin-based artists who use video games and related genres as a medium for art and critical reflection. Monthly artist presentations are accompanied by artist films and documentaries that relate to games, sport, technology, art, and eSport, considered as mirror and parade ground of digital networked society.</p>
