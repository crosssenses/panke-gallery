	                <p><a href="http://monotrail.blogspot.de/"><strong>Monotrail</strong></a> <br>is the latest musical project of Berlin-based Dutch artist and musician Rijnder Kamerbeek. Improvising on a modular synthesizer Monotrail sculpts his sounds with analog noise, distorted synths, smooth filtered sequences and experimental percussion. The result is a sonic journey which takes you along melodic ambient and drone sessions, distant field recordings, and pulsating rhythms.</p>

					   <br>

					     <div class="image-wrapper 3-col">
                        	<img src="/img/soundnight/soundnight4/itskiny.jpg" />
                    	</div>


	                <p><a href="http://antiparti.cl"><strong>itskiny</strong></a> <br>- a Catalina is a strange spill of worldly magnitude. After being a Scientist in Southern California, a Doctor in South India, a Time-Based Artist in Australia, and currently apprentice to a Japanese woodcrafts master in Tokyo, a secret project continues to brew in the musky and ethereal underground that is the Berlin noise scene. Using circuit bent, naively crafted, and high-tech programmable devices the goal is to have a sweetly enveloping lull train your brain to master the ability to sleep walk through a set. Maybe it’s born with it , maybe it’s a JigglyPuff</p>


					   <div class="image-wrapper 3-col">
                        	<img src="/img/soundnight/RCO.png" />
                    	</div>


	                <p><a href="http://www.i-a-m.tk/r-c-o.html "><strong>RCO</strong></a> <br>is an artist collective that wires up everyday objects and that develops numerous circuitry to generate and experiment in sounds and noises. A variety of devices and noise sources are arranged in interactive feedback-loops and polyphonic sound installations.
Live RCO assembles momentary and improvised setups that consider specifics of the particular space and its situation and integrates these into the continuously changing acoustical environment.
</p>
	                <br>


						<div class="image-wrapper 3-col">
                        	<img src="/img/soundnight/soundnight4/Taraficon.jpeg" />
                    	</div>

                   <p><a href="http://ioanavrememoser.com/post/167770192597/taraficon-electronic-harmonica-klangobjekt-2017"><strong>Ioana Vreme Moser</strong></a> (RO 1994) <br>is an audio-visual artist, performer, hardware-hacker, researcher and instrument builder engaged with transmedia narratives and electronic poetics.<br>
By examining the correlations and the subtle boundaries in between the perceptible optical and audio mediums, she uses natural materials, retro-machinery and electronic technologies to explore the links in between objects and their memory, exposing issues of conspicuousness and of exile within the world.
Her work, currently involves electro-mechanical instruments, sound sculptures, kinetic installations, audio-visual performances, graphic notations, softly spoken recordings, data compendiums and video pieces.</p>

						<br>

				   <div class="image-wrapper 3-col">
                        	<img src="/img/soundnight/soundnight4/KarstenHuebner.jpeg" />
                    	</div>

	                <p><a href="http://matchees.blogspot.de/p/music.html"><strong>Klaas H&uuml;bner</strong></a> <br>is a Berlin-based sound sculptor, improviser, and instrument-builder whose installations coax out uncanny chants from whirly tubes, ceiling fans, styrofoam, and cassette tapes. His work made him travel, exhibit and perform in theater and dance pieces, art shows, project spaces and performance places in Guangzhou, Kairo, Seattle, New Orleans, New York, Los Angeles, Venice, Bolzano, Z&uuml;rich, Barcelona, Paris, London, Lodz and many more. He frequently visits New Orleans, where he works with the Music Box Project and is active in the Berlin improvising and experimental music scene with his unique instrument the &quot;tape guitar&quot; and his music project on electromagnetic fields &quot;Smog&quot;.<br> </p>

