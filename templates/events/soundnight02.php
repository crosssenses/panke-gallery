	                <p>Avant-garde German visual artist and composer <a href="http://www.raster-media.net/artists/robert-lippok"><strong>Robert Lippok</strong></a> has been an influential player in Berlin’s thriving experimental music scene from a very early age. In 1983, he co-founded dissident punk band Ornament und Verbrechen with his brother Ronald, inspired by industrial trailblazers Throbbing Gristle. An open platform to explore jazz, electronic, and industrial concepts with like-minded collaborators of the East German underground persuasion, the group remained active until the mid-1990s, at which point it was overtaken by the brothers’ next and most well-known collaboration, the palindromic To Rococo Rot. A significant post-rock/electronic outfit started by the Lippok brothers and Düsseldorf bass guitarist Stefan Schneider, the Krautrockish band ushered in a new generation of electronic acts committed to acoustic investigations and improvisation of all stripes. Known for his expansive imagination, inventive rhythmic reflexes, and layers of fuzzy tones, Lippok’s solo work is just as wide-ranging – from funky, glitch-y, twisted techno record Redsuperstructure for Raster-Noton (2011), to stage design for operas, gallery exhibitions, and notable collaborations with Italian harpist Beatrice Martini, Canadian percussionist Debashis Sinha or brother Ronald and Italian composer Ludovico Einaudi (the latter trio as Whitetree). </p>

	                <br>

	                 <div class="image-wrapper.fit-height">
                        	<img src="/img/soundnight/soundnight02/Candy%20Blissett_Omsk%20Social%20Club.png" />
