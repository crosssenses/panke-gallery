	                <p><a href="https://soundcloud.com/user-185408115"><strong>Candy Blissett</strong></a> creates a genre of post-political entertainment for listeners both on and offline. She was formally known as VONVERHILLE and PUNK IS DADA. Their soundscape poetics/ audio essays have featured heavily in Omsk Social Club's installations over the last few years in both art institutions and project spaces globally, such as The Migros Museum (Zurich), Gold+Beton (Cologne), Middlesbrough Modern Art Museum (UK) Exgirlfriend (Berlin) and Center (Berlin).</p>

