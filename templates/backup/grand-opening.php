                    <h3>About the exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                    <p>For its first exhibition, panke.gallery is putting two Berlin-based artists in dialogue with one another. Daniel Keller and Daniel Pflumm both developed their own formal languages out of connections with club culture, either directly or through its extended environment, thus creating work that in both cases is exemplary for its generational milieu.<br>
The two artists are linked through their engagement with corporate aesthetics, the world of advertising, prosumers and their brands, icons, and symbols. They deconstruct the aesthetics of multinational corporations, which dominate, even devour, the fields of communication and creativity in their ever-expanding appropriation of the world. Their artistic concern with the futuristic techno-utopias of capitalism does not, however, lead them to visions of a cypherpunk dystopia, but rather uses the ambivalent, sometimes ironic mode of Pop as a mode of resistance. As squats turned into clubs, clubs into labs, labs into startups and startups into AirSpace, both artists not only accompanied these developments and critically commented on them but also staged interventions within this changing context through their work. In the exhibition their practices are represented by a concentrated selection of works that are exemplary in their reflection on the intersections of club culture, music, art, commodification and branding.<br>
This inaugural exhibition also speaks to panke.gallery’s focus on the intertwined histories of digital or netbased art activities and their relation to sonic/sound/poetry and club culture.</p>
