                    <h3>Artists</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p><strong>Daniel Keller</strong> (b. 1986), was born in Detroit and  lives and works in Berlin. Daniel Keller’s work exists at the intersection of technology, economics, culture, and collaboration—by absorbing forms found in nature for mixed media works, he illuminates “the tension between urban planning and an organic spatial landscape.” Integrating elements as disparate as concrete, wood, and resin with web-related works, he evokes the disparity between physical and digital space. The artist often opposes innately human instincts, such as the nomadic impulse found in backpacking or a bent toward symmetry, with environmental phenomenon like organic growth and symbiotic relationships. Keller critically addresses notions of industrial progress and the ways in which a “prosumer imagineer artist” might be both a technological disruption and reaction. Keller founded AIDS-3D, an artistic collaboration with Nik Kosmas, which further explores the potential of “techno-utopianism.” <a href="http://www.artspace.com/daniel-keller">(Artspace)</a></p>
                    <p><strong>Daniel Pflumm</strong> born 1968 in Geneva, Switzerland lives and works in Berlin.
Pflumm became known through his light-boxes which dissected the logos of brand names, and through his videoloops which reduced the dramaturgy of TV commercials to their essentials. He founded a company which was listed under various names but did not produce anything but self-represenations by logos and symbols. Pflumm strips media art of its last residues of fragmentary statements and transforms it into something that can be placed within the art as well as the club context. <a href="http://www.medienkunstnetz.de/kuenstler/pflumm/biografie/">(medienkunstnetz)</a></p>
