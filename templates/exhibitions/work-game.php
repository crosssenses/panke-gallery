            <h3>Artists</h3>
        </div>


        <div class="col-md-8 col-sm-8 col-xs-12">
             <p><strong><a href="http://www.rolux.org">Sebastian L&uuml;tgert</a> (a.k.a. Robert Luxemburg) </strong>lives and works in Berlin as an artist, writer and programmer. He is the co-founder of Bootlab (2000-2007), Pirate Cinema Berlin (2004-), 0xDB (2007-) and Pad.ma (2008-), and was the co-organizer of several international conferences, like The Dictionary of War (Frankfurt-Munich-Graz-Berlin 2006-2007), The Oil of the 21st Century (Berlin 2007), The Summit of Non-Aligned Initiatives in Education and Culture (Berlin 2007) and Properties of the Autonomous Archive (Bombay 2011). His work has been shown in numerous exhibitions, among them Animations (PS1 2001, Kunstwerke 2002), Werkleitz Biennale (2004), Poor Man's Expression (Kino Arsenal 2004, Artists Space 2012), Van Abbe Museum (2006), Manifesta (2008), Gwangju Biennale (2008), Taipei Biennale (2008), Artists Space (2012), Documenta (2012, with Pad.ma), Istanbul Biennale (2015) and Pane per poveri (2017).</p>
            <p><strong><a href="http://www.sebastianschmieg.com">Sebastian Schmieg</a> </strong>examines the ways networked technologies shape online and offline realities, in artworks that range from shredded hard-drives from a Google datacenter to crowd-sourced versions of popular self-help books using Amazon's Kindle. His output encompasses websites, interface performances, algorithmic videos, online interventions, print-on-demand books or neural networks. He focuses on a critical engagement with the manifold and ubiquitous nodes of the Internet – both human and technological – as sites of the political. Previously his work has been exhibited at Transmediale, Berlin, Germany; Art Center Nabi, Seoul, South Korea; Bitforms Gallery, New York, USA; and The Photographers’ Gallery, London, UK. Sebastian Schmieg lives and works in Berlin.</p>
        </div>
