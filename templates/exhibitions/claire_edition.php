<p>Since 2013, much of Claire Tolan’s work has found its foundation in the
sounds of ASMR, Autonomous Sensory Meridian Response. Tolan has hosted
"You’re Worth It,“ an ASMR show on Berlin Community Radio for three
years. She has also worked with ASMR in performance, recording, and
exhibition, collaborating with musician Holly Herndon, writer and artist
Inger Wold Lund, and artist Camilla Steinum, among others. Tolan was the
2015 Goethe-Institut and SPACE Perlin Noise sound artist-in-residence at
London’s White Building and a 2016 Berlin Musicboard Fellow. At present,
Tolan is developing an ASMR-inspired tabletop role-playing game (RPG)
system, die Siedler von SHUSH.</p>

