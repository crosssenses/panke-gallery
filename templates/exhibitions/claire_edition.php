                    <h3>About the Edition</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p>Panke.gallery's first edition features artist Claire Tolan with her work SHUSH Tones, a series of system sounds and ringtones for iOS and Android.</p>
                    <p>For this edition Claire Tolan produced <em>EXCLUSIVE</em> sets of new SHUSH TONES!  <h3>SHUSH Basilisk - Truth To SHUSH - Bayesian SHUSH - Rare Earth Tones - SHUSH Vuln - SHUSH Yoke</h3></p>


                    <p>“SHUSH Tones were created by SHUSH SYSTEMS in the early autumn of 2015. SHUSH
SYSTEMS predicts SHUSH, a nascent fork of ASMR, and develops strategies for the deployment of SHUSH upon its arrival in our world. SHUSH will come as an answer to the crippling sublime that the individual experiences in the face of complex, planetary-scale systems &ndash; climate (and its change), finance, computing. SHUSH will rebase our megastructures, refactor our systems, and reformat our consciousnesses. SHUSH demands the end of this world and the inauguration of the next one. And yet, for now, there it remains, hanging on the horizon, always approaching but never arriving, crystalised as a to-come. SHUSH is left-behind even as it becomes later and later for its scheduled appointment. What is up with SHUSH? We appreciate your feedback as together we await SHUSH-to-come. You can reach us by mailing HUSH at sign SHUSH dot sign SYSTEMS.” (<em>from the website’s description</em>)</p>
<p>Edition #001 SHUSH Tones by Claire Tolan is published as a limited edition of 100. It is the first of a series of planned artist's editions and is made up of a designed cover and an inlay with the download code for the artwork. You can choose your price from one of three options among the numbered editions: 1-20 are available for the supporters' price of €100; 21-70 cost €70, and 71-100 can be acquired for €35 each.
All proceeds are shared between the artist(s), designer(s) and distributor(s) with a portion going towards the operating costs of panke.gallery.
We see the editions as a possibility to support the artists and panke.gallery. The design of the editions is intended emphasise its character as a collectible digital object and illustrate the work of the featured artist. For the first edition we are glad to have had Anna Luise Lorenz and Ingmar Spiller as the design team. </p>

<p> In the work /SHUSH Tones/, Claire Tolan infiltrates the mobile devices of her audience with a series of ringtones that are available exclusively as panke.gallery's Edition #001. The mobile phones that are ubiquitous our daily lives become vessels for Tolan’s ongoing experiments with ASMR. The resulting tones disrupt everyday situations in an uncanny and often irritating way. Through this artistic intervention, Tolan explores the vulnerability of technical systems. With /SHUSH Tones/ she uses the YouTube phenomenon of ASMR and its subversive power to point to the threatening aspects of the mobile devices we use every day. She thus addresses the surveillance and control by multinational corporations and governmental agencies and how it extends into our private lives. Ringtones, long used to signal brand-allegiances or personal pop preferences, here receive a critical reinterpretation. What results opens up creative and empowering possibilities in one of mass culture's dead zones -- the under-attended space of the alert.</p>

