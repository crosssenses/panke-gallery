                    <h3>&uuml;ber die Ausstellung</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                    <p>Horst Bartnig arbeitet seit Mitte der 60er Jahre als Maler und Graphiker. Seine frühen computierten konkreten Formfindungen sind das Resultat von künstlerischen Untersuchungen, die vorausahnend die Ästhetik der algorithmisch generierten Computergrafiken in der digitalen Kunst und ihre folkloristischen Erscheinungen im Internet vorwegnahmen. Alma Alloro ist eine junge Künstlerin, deren künstlerische Arbeit von der Demo- und Hackerszene der 80er und 90er Jahre inspiriert ist. Ihre Arbeiten basieren auf einem Verständnis von Kunst als einer angewandten und populären künstlerischen Praxis in der Tradition des Bauhauses.</p>
<p>Die Formkombinationen von Horst Bartnig folgen einer Fragestellung, die sich mit der Relationen der Formen wie auch mit den Wahrnehmungsvorgängen beschäftigt. Die Variationen sind die experimentelle Realisation einer Regel, in deren Entfaltung als Reihung im Raum sie im Kopf des Betrachters wie in einem Film zusammen laufen. <br>Alma Alloro entwickelt ihre Arbeiten anhand von eigenen Animationen oder Cracktros der Demoszene, die sie in die Fläche zurückbringt, indem sie jedes Frame als Einzelbild auf einer Bildfläche neu anordnet, einreiht und entzeitlicht. </p>
<p>Die Ausstellung concrete::dynamic stellt beide Künstler und ihre Arbeiten in einen Dialog. Indem sie die ästhetische Aktualität ihrer Formensprache demonstriert, verweist sie zugleich auf die Bedeutung der Traditionen von Bauhaus und Konkreter Kunst für heute.
</p>
                </div>
                 <div class="col-md-4 col-sm-4 col-xs-12">
                    <h3>About the exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                    <p>Horst Bartnig has worked as a painter and graphic artist since the mid 60s. His early concrete works with computer-generated forms are the result of artistic investigations that anticipated the aesthetic of algorithmically generated computer graphics in digital art and their importance within the digital folklore of the internet.< br> Alma Alloro is a young artist whose work is inspired by the demoscene and hacker culture of the 80s and 90s. Her work comes out of an understanding of art as an applied, vernacular practice in the Bauhaus tradition.</p>
<p>The combinations of form in the work of Horst Bartnig emerge from a line of inquiry that emphasises the relations among forms as well as the processes of perception itself. The variations in his work are the experimental outcome of a rule that unfolds as a series in space – leading to their convergence in the head of the viewer, as if in a film. Alma Alloro’s work is developed out of her own animations or cracktros from the demoscene, which she brings back into a singular image plane by turning each frame into an individual image, rearranging and reordering them and taking them out of their chronological sequence.</p>
