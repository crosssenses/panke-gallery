                    <h3>Artists</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p><strong>Alma Alloro</strong> ist eine israelische Künstlerin, die in Berlin lebt. Sie studierte an der Midrasha School of Art in Israel und absolvierte 2011 mit einem MFA den postgradualen Studiengang „Kunst im öffentlichen Raum und neue künstlerische Strategien“  an der Bauhaus Universität, Weimar. Ihre Arbeiten, welche Handwerk und Animationen umfassen wurden in der Transfer Gallery – NYC, The Store – Dresden, Museum für angewandte Kunst – Frankfurt am Main, Athens Digital Arts Festival, Western Front – Vancouver, Musrara-mix festival – Jerusalem, Furtherfield Gallery – London, Neues Museum – Weimar gezeigt.</p>
                    <p><strong>Horst Bartnig</strong> ist ein zeitgenössischer deutscher Maler, Bühnenmaler und Grafiker, der in Berlin lebt. Seit Mitte der 60ziger beschäftigt er sich mit Konkreter Kunst. Daneben war er als Bühnenmaler für das Deutsche Theater Berlin und das Berliner Ensemble tätig. Um die komplexeren Berechnungen von Bildfolgen durchzuführen, arbeitete er in den 70ziger Jahren mit einem Physiker des Zentralinstituts für Kernforschung Rossendorf bei Dresden zusammen und nutzte die Großrechenanlagen in Berlin-Adlershof. 2001 erhielt er den Hannah-Höch-Preis.
</p>
