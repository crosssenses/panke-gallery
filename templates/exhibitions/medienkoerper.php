                <div class="col-md-4 col-sm-4 col-xs-12">
                    <h3>Artists</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p><a href="http://www.chabrowski.info"><strong>Yvon Chabrowski</strong></a>, born in East-Berlin, studied photography with Timm Rautert
and Florian Ebner at the Academy of Fine Arts Leipzig and the École nationale supérieur des beaux-arts Lyon. She graduated from Peter Piller`s Meisterklasse.
<br>Her work deals with the visual formulas of contemporary media, which she decontextualizes, making them graspable by creating a reflexive distance. Her video installations thus create awareness for the grammar and manipulation strategies of omnipresent media images. </p>
                    <p><a href="http://www.daniploeger.org"><strong>Dani Ploeger</strong></a> combines performance, video, programming and hacking to investigate and subvert the spectacles of techno-consumer culture. Among others, he has worked with traditional metal workers in the old city of Cairo to encase tablet computers in plate steel, attended firearms training in Poland to shoot an iPad with an AK47, made a VR installation while embedded with frontline troops in the Donbass War, and travelled to dump sites in Nigeria to collect electronic waste originating from Europe. He holds a PhD from the School of Media, Film and Music at the University of Sussex and is currently a Research Fellow at The Royal Central School of Speech and Drama, University of London. He works from his studio in Vlissingen on the Dutch seaside.</p>
</p>
