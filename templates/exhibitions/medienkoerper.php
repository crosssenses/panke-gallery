                    <h3>about the exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                    <p>Dani Ploeger will premier his work "The Grass Smells So Sweet". <em>The grass smells so sweet</em> is a VR installation with audiovisual simulations and intimate narrations based on a series of Quora and Reddit responses to the question: ‘How does it feel to get shot in the head?’</p>
<p>Yvon Chabrowski will show the works TOUCHING THE IMAGES and SCREEN.<br>
TOUCHING THE IMAGES  goes back to the question, how we touch images and results from thought that the image-touching itself becomes a gesture and a picture.
<br>SCREEN shows a performer life-size in the space afforded by a 40-inch monitor. Within an area of 92 x 55 cm, she touches, taps, and presses against the surface of the monitor from the inside. Her body bends and winds in the narrow space.
The medial image-space is presented to the viewer as a real space. Although the screen demarcates the medium’s insuperable limit and stakes off the realm of the outside world, the image nevertheless has a profound impact on reality.
(Yvon Chabrowski, 2016/17)
</p>
                </div>

