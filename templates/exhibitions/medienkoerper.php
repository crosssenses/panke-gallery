         <section class="info">  <!-- Info -->
            <div class="row">

	            <div class="col-md-4 col-sm-4 col-xs-12">
                    <h3>&uuml;ber die Ausstellung</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                    <p>Die Ausstellung „MedienK&ouml;rper“ besch&auml;ftigt sich mit der Aufl&ouml;sung des vermittelnden Charakters von Medien durch eine zunehmend wachsende intime und immersive N&auml;he zu ihren Nutzern.<br>
Der vermittelnde Charakter der Medien, verstanden als elektronische Ger&auml;te, verschwindet je mehr das Ger&auml;t durch die N&auml;he zum Nutzer transparent wird. Die sogenannten Neuen Medien treten uns nicht mehr als vermittelnde Ger&auml;te entgegen, sie treten immer dichter an uns heran. Unterst&uuml;tzt durch ihre sensorischen Interfaces entwickeln wir eine private, beinahe intime Beziehung zu den Ger&auml;ten, die uns in Ber&uuml;hrungen oder immersiven Bild- und Klangwelten nicht nur sich vergessen lassen, sondern auch unsere Eigenwahrnehmung ersetzen. Die dabei entstehende Transparenz wirkt zugleich in zwei Richtungen: W&auml;hrend der K&ouml;rper in der Ber&uuml;hrung mit dem Ger&auml;t mit diesem verschmilzt, verschwindet zugleich das Ger&auml;t als ein Gegen&uuml;ber. Bildwelten, Textstreams, Klang- und sensorische Eindr&uuml;cke &uuml;berfluten uns und f&uuml;llen die Denkr&auml;ume mit Bildern und Texten aus der Kommunikationsgesellschaft, einer Netzwerkgesellschaft verschalteter Ger&auml;te, in der individuelles, sogenanntes authentisches Erfahren des eigenen Daseins scheinbar nur noch im Extremen oder im Spiegel des Anderen m&ouml;glich ist. <br>

Durch neue sensorische Interfaces und immersive Bild- und Klangwelten entsteht heute eine neue Qualit&auml;t der Interaktion von Mensch und Medium. Dies wirft einerseits die Frage auf, welche Entwicklung die Vermittlerfunktion noch nehmen wird. Andererseits verweist die neue Interaktionsqualit&auml;t auf die Frage, wie sich in der Verschmelzung mit dem Medium, individuelle Selbstvergewisserung noch realisieren l&auml;sst. <br>

So behandelt Yvon Chabrowski in Ihrer Arbeit  „Touching The Images“ die sinnliche Beziehung zwischen Medium und Nutzer in ihrer Ambivalenz und in ihrer Qualit&auml;t der Bildproduktion in der sinnlichen Erfahrung. In Ihrer Arbeit „Screen“ untersucht sie den Ger&auml;te-Bildraum und seine Grenzen. <br>

Dani Ploeger hingegen stellt in seiner Arbeit "The Grass Smells So Sweet" die Frage nach den Grenzen und M&ouml;glichkeiten der Vermittelbarkeit von Extremsituationen. Die als VR Installation ausgef&uuml;hrte Arbeit basiert auf den intimen Beschreibungen in den Foren von Reddit und Quora zu der Frage „How does it feel to get shot in the head?“ Die Installation nutzt dabei nicht nur die Virtuelle Realit&auml;t als eines der im Moment immersivsten Medien, sie bezieht auch klassische Medien wie Film und Sound mit ein, um die M&ouml;glichkeit der Vermittlung existenzieller Momente auszuloten. </p>
