                 <div class="col-md-4 col-sm-4 col-xs-12">
                    <h3>about the exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p> #idnSTREETart — streetArt meets net.art</p>
                    <p style="font-weight: 300">
This exhibition puts the Berlin street artist behind the familiar white “<strong>6</strong>” in dialogue with the Belgian/Dutch net art collective <strong>JODI</strong>. They are connected by how they see urban space and the internet, respectively, as a canvas and screen for their art. The city and online space both function as an arena for aesthetic investigations and experiments. <strong>JODI</strong>’s IDN domain names, reduced to single letters or symbols, create the most obvious aesthetic commonality between the two artists and here offers a starting point for drawing out the connections between the two bodies of work. Beyond their material aesthetic, the artists’ attitude in both cases grows out of a conviction in the importance of gestures of self-determination and individual agency. In other words, with their performative actions JODI and !@SeX#─────██████████════█ take a stand against the mainstream, the loss of freedom and of opportunities for participation, as well as against the disappearance of public space, both in the city and online, as it is increasingly co-opted or colonized by multinational corporations.
Although both net art and street art intrinsically resist the gallery context, the panke.gallery nonetheless is making the attempt to present these two positions together, starting out from the IDN domain works as a way to link the practices of these artists as they have developed over the past twenty years.</p>
