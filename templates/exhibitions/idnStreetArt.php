                    <h3>&Uuml;eber die Ausstellung</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p> #idnSTREETart — streetArt meets net.art</p>
                    <p style="font-weight: 300">1996 taucht in Berlin erstmals die <strong>6</strong> als gemaltes Zeichen auf. Seitdem findet man sie in Berlin auf Plakaten, B&uuml;rgersteigen und auf an unterschiedlichen Stadtbrachen &uuml;brig gebliebenen Materialen. Auch auf vom K&uuml;nstler erstellten tempor&auml;ren Installationen bildet die <strong>6</strong> den zentralen Bezugspunkt. Aus der hektischen Mitte der Neunziger Jahre geboren sind die Tags des Berliner K&uuml;nstlers ! @SeX#─────██████████════█ nicht nur in ihrer Quantit&auml;t effektiver und moderner als die herk&ouml;mmliche StreetArt. Die <strong>6</strong> Linie l&auml;sst sich laut Aussage des K&uuml;nstlers gut vom Fahrrad aus malen. Sie besetzt den Stadtraum und ist als performativer Akt einer malerischen und dynamischen Geste zugleich Zeichensetzung, die als eine malerische Geste auf die Anwesenheit des K&uuml;nstlers verweist. Sie besetzt den Stadtraum und ist als performativer Akt einer malerischen und dynamischen Geste zugleich Zeichensetzung, die auf die Geste wie auf die Anwesenheit des Künstlers verweist. Dabei besetzt er durch den ephemeren Charakter der Materialien, auf denen die <strong>6</strong> erscheint, diese immer nur temporär und wird dadurch veranlasst den performativen Akt beständig zu wiederholen.
                    Fast zeitgleich mit dem Aufkommen der <strong>6</strong> im Stadtraum, richtete ! @SeX#─────██████████════█ Domains ein und setzte den &ouml;ffentlichen Stadtraum in Analogie zum digitalen Raum im Netz, indem er die Domainnamen in den Stadtraum malte und auf den Domains Dokumentationen von seinen Arbeiten in den Stadtr&auml;umen ver&ouml;ffentlichte. Dieses gegenseitige Aufeinander verweisen zeugt für den Künstler von der Gleichwertigkeit virtueller wie realer Räume in Bezug auf ihre realitätsbildende Funktion. Sein Werk umfasste teilweise mehr als 350 URLs bzw. Domainnamen, deren gestalterisches Material sich zeitgleich zur Entwicklung des Netzes ver&auml;nderte. Beginnend mit den <i>ASCI-Graphiken</i> (Eule, Smily etc) &uuml;ber die Reduktion auf ein einzelnes graphisches Zeichen aus dem <i>IDN-Namensbereich</i> (Pfeil, Dreieck etc.) bis hin zu den farbigen <i>emoji-Domains</i> (rotes Herz, gelber Smiley etc.) nutzt ! @SeX#─────██████████════█ das Zeichenmaterial, um mit der &auml;sthetischen Qualit&auml;t von Domainnamen seinen eigenen k&uuml;nstlerischen Ausdruck zu schaffen.</p>

                    <p style="font-weight: 300">Im Jahr 1996 beginnt die Hochphase der Netzkunst, die 1997 ihren <i>Peak</i> im noch jungen Internet erlangt. Dabei nimmt das K&uuml;nstlerkollektiv <strong>JODI</strong> eine wichtige Position ein. Einerseits bildet in seinem Gesamtwerk die Untersuchung des Materials des Internets auf seine k&uuml;nstlerischen und bildhaften Qualit&auml;ten einen wesentlichen Aspekt ihrer Arbeit. Anderseits impliziert diese stets auch gesellschaftliche und kulturelle Setzungen. So wird seit Beginn ihrer Arbeit die URL-Zeile zur Bildfl&auml;che, d.h. zum zun&auml;chst leeren Blatt. Gleichzeitig ist die Registrierung einer URL ein komplexer performativer Akt bestehend aus &ouml;konomischen, juristischen und politischen Implikationen.</p>

                    <p style="font-weight: 300"><strong>JODI</strong> nutzt seit Beginn der k&uuml;nstlerischen Auseinandersetzung mit dem Internet den Code als Bildzeichen. In der ikonischen Arbeit <i>%location</i> (1994 − 1997) untersuchen sie die komplexen Tiefenschichtungen einer Netzkunstaktivit&auml;t mittels der &auml;sthetischen Form auf einen k&uuml;nstlerischen Ausdruck hin. Zur selben Zeit entwickeln sie einen spezifischen Stil, bei dem die &auml;sthetische Komponente der Codezeichen in den Vordergrund r&uuml;ckt. Als circa 2008 die URL zusehends verschwindet, da durch die Kolonialisierung des Internets durch die Konzerne und die Einf&uuml;hrung mobiler Ger&auml;te und Betriebssysteme die Browserleiste in den Hintergrund r&uuml;ckt, setzt sich <strong>JODI</strong> diesem Verdr&auml;ngungsprozess mit einer vermehrten Produktion von Arbeiten dagegen. So wird in ihrer aktuellen Arbeit <i>idn.jodi.org</i> auf das komplexe Zusammenspiel von Bildzeichen, Symbol, Buchstabe, Name, Besitz, Server-Raum und File-Struktur verwiesen.</p>

                    <p style="font-weight: 300">Die Besch&auml;ftigung mit dem &auml;sthetischen Potenzial der Zeichen, ihrer Reduzierbarkeit und ihren immanenten kulturellen und politischen Implikationen verbindet das K&uuml;nstlerkollektiv <strong>JODI</strong> mit dem Berliner K&uuml;nstler ! @SeX#─────██████████════█. Innerhalb der Netzkunstbewegung markiert das Jahr 1999 einen wichtigen Moment. W&auml;hrend der Geburtsstunde von <i>Google</i> endet das Experiment der <i>Internationalen Stadt Berlin</i>, einer Utopie von einer freien, kreativen, politischen und sozialen Plattform im Internet. Gleichzeitig fand der sogenannte <i>TOYWAR</i> statt, in dem der Konzern <i>eToys Inc.</i> die K&uuml;nstlergruppe <i>etoy.com</i> auf Herausgabe ihrer Domain verklagte. <i>Myspace</i> (2003), <i>Facebook</i> (2004) und <i>Youtube</i> (2005) markieren den &Uuml;bergang in das sogenannte <i>PostInternet</i>, in dem die sozialen Plattformen, den Shopping Malls gleich, das ehemals chaotische, offene und experimentelle Internet domestizierten. <strong>JODI</strong> und ! @SeX#─────██████████════█ stellen sich mit ihren individuellen performativen Akten bis heute r&uuml;ckhaltlos gegen den Mainstream, die Verdr&auml;ngung von Freir&auml;umen und von Partizipation sowie gegen das Verschwinden des &ouml;ffentlichen Raums, der Stadt und Netz umfasst und fortschreitend durch multinationale Konzerne assimiliert wird.
                    </p>
