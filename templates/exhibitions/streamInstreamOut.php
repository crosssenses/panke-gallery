        <div class="col-md-8 col-sm-8 col-xs-12">
             <p><strong><a href="http://www.jeron.org">Karl Heinz Jeron</a> </strong> was born in Memmingen, Germany in 1962. He lives and works in Berlin since 19987. His art deals with every day culture, media perception and information processing. He rather creates experiences than objects. Jeron's works span from the singing and dancing robot Sim Gishel, to compositions for electrified rotten vegetables and audio tour guides in onshore wind farms. Recent exhibitions, events and collaborations include ZKM Karlsruhe, Ars Electronica Linz, Documenta X, ICA London, Walker Art Museum Minneapolis, Berlinischen Galerie Berlin and the Museum of Modern Art San Francisco.
<br>
Seine Arbeiten wurden unter anderem im ZKM Karlsruhe, auf der Ars Electronica Linz, der Documenta X, am ICA London, dem Walker Art Museum Minneapolis, der Berlinischen Galerie Berlin und dem Museum of Modern Art San Francisco gezeigt.</p>
            <p><strong><a href="http://asdf.us">Jules LaPlace</a> </strong>  has been making ASCII art and color codes since 1994. Over the years he has hidden a lot of crafty text drawings in the source code of various apps and websites, many now hosted on the url asdf.us, as well as making stealth net art image editing software called Photoblaster.  A few years ago whilst spending some time browsing the web during his off hours, he met a visitor from the future who warned him that programming would soon be less important than knowing how to draw.  To this end he built a color ASCII editing tool which he called ASCIIBlaster, designed to be used within the mysterious visual-based chatrooms of the cyber elite.  Impressed with what a little ASCII could do, he slowly became obsessed with the project, developing it late at night in his secret engineering B.B.S. laboratory.  ASCII art is lots of fun, you will agree, but even more so when you can paint with it in full color and explore all sorts of neat, phun, phreaky kewl effects- and even better when you can draw with friends!! Please save often and look out for some cool easter eggs as always. Hope you enjoy!!!1!
</p>
