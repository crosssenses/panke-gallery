                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p><strong>Jonas Lund</strong> 1984, born in Sweden, based in Berlin and Amsterdam.

Jonas Lund is a Swedish that creates paintings, sculpture, photography, websites and performances that critically reflects on contemporary networked systems and power structures of control. He earned an MA at Piet Zwart Institute, Rotterdam (2013) and a BFA at Gerrit Rietveld Academy, Amsterdam (2009). He has had solo exhibitions at Whitechapel Art Gallery, London (2016), Steve Turner, Los Angeles (2016, 2015, 2014), Växjö Konsthall Sweden (2016), Boetzelaer|Nispen, Amsterdam (2014), Showroom MAMA, Rotterdam (2013), New Museum, New York (2012), and has had work included in numerous group exhibitions including at Carrol/Fletcher, London, ZKM, Karlsruhe, Van Abbemuseum, Eindhoven, Witte De With, Rotterdam, De Hallen, Haarlem and the Moving Museum, Istanbul. His work has been written about in Artforum, Kunstforum, Metropolis M, Artslant, Rhizome, Huffington Post, Furtherfield, Wired and more.</p>
