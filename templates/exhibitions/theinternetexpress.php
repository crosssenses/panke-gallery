                    <h3>About the exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                   <p>Panke.gallery is proud to present - The Internet . Express - a new work by Jonas Lund. We have produced this net-art sculpture with the kind support of the transmediale festival. The piece comes in the form of a Browser Game and lets us, the users, drive on the information-super-highway. The only impediment is a constant barrage of advertisements which slow one down eventually causing one to lose the game. The experience reflects, on many levels, what the internet has become today. The advertisements have not only turned it into a meaningless shopping mall, they also jeopardize our security, leading us to crash. The physical design calls to mind a hybrid arcade console and cash machine. And that is what the Internet seems to be now. But strangely our cognitive dissonance won't let us stop playing - look at all the fun we have just by trying to cope ... This event was part of the Wedding Kultur Festival. </p>                </div>

