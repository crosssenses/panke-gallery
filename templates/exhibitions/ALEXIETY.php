                    <p><strong><a href="http://www.punkisdada.com/">Omsk Social Club</a></strong> Lives and works in Berlin. She has exhibited across Europe in various institutions, galleries and offsites such as Migros Museum für Gegenwartskunst, Zürich, Middlesbrough Modern Institute of Art, Bergen Kunsthalle, Gold and Beton Cologne, “Vomit Apocalypse” for Glasgow International and Kunsthalle Zürich. She attended The Eternal Internet Brother/Sisterhood residency in Sri Lanka in 2016 curated by Angelo Plessas and was awarded the Gleis 70 Zurich international artist award from Pro Helvetia in 2017 and the 8th ARTWARD Junior Prize in Munich. <br>
 <br>
Her theoretical essays and creative texts have been commissioned for Kaleidoscope Magazine, NXS, Badlands, A little disaster, NRW Forum Düsseldorf Status Effect curated by Andrew Varano, Websafe2k16, Oncurating.org  - “Alienation and Estrangement Issue” edited by Dorothee Richter. She was also recently included in the book Polar Reflex for the exhibition “The Internet is Present” edited by Reese Riley. <br>
<br>
Omsk Social Club coined and works heavily with the concept of Cosmic Depression – The theory of depression caused by digital utopia (Paradise without Ecology) and Real Game Play. Omsk’s practice spans from visual to theoretical production often using the model of gaming. Working with a cross disciplinary practice and over many avatars or pseudo-names. Omsk Social Club is constantly observing and questioning the concept of Self, Individualism and the community both in off and online scenarios. Pioneering the spectacle Ying Colosseum in Berlin (2015-2016) and Play Co Summit with Ed Fornieles (2017-ongoing), She is heavily influenced by her peers and their voices as collective anonymous mass often incorporating them into her Meta Larps and Real Games. One may liken her output to a new form of relational aesthetic practice.<br>
 <br>
<em>“Zen, Speed, Organic: 3 lifestyle diets.”</em>
