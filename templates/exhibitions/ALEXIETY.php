 <p><strong><a href="https://wwwwwwwwwwwwwwwwwwwwww.bitnik.org">!MEDIENGRUPPE BITNIK</a></strong> (read - the not Mediengruppe Bitnik) are contemporary artists working on, and with, the Internet. Their practice expands from the digital to physical spaces, often intentionally applying loss of control to challenge established structures and mechanisms. In the past they have been known to subvert surveillance cameras, bug an opera house to broadcast its performances outside, send a parcel containing a camera to Julian Assange at the Ecuadorian embassy in London and physically glitch a building. In 2014, they sent a bot called «Random Darknet Shopper» on a three-month shopping spree in the Darknets where it randomly bought items like keys, cigarettes, trainers and Ecstasy and had them sent directly to the gallery space. !Mediengruppe Bitnik’s works formulate fundamental questions concerning contemporary issues. <br>
 <br>
Their works are shown internationally, most recently in exhibitions at Annka Kultys Gallery London, House of Electronic Arts Basel, Eigen + Art Lab Berlin, Super Dakota Brussels, Centre Culturel Suisse Paris, Aksioma Ljubljana, Kunsthaus Zurich, FACT Liverpool, Onassis Cultural Center Athens, Public Access Gallery Chicago, Kunstverein Hannover, Nam June Paik Art Center South Korea, Fondazione Prada Milano, Shanghai Minsheng 21st Century Museum, The Pushkin Museum of Fine Arts Moscow, Cabaret Voltaire Zurich, Beijing Contemporary Art Biennial and the Tehran Roaming Biennial.
</p>
            <p><strong><a href="https://soundcloud.com/low-jack">LOW JACK</a></strong>/ Philippe Hallais is a French electronic music artist born in 1985 in Tegucigalpa (Honduras) and living in Paris. His music plays with the re-appropriation of sonic clichés, media folklores, and the multiplicity of musical languages associated with dance subcultures. He has published three albums under the pseudonym Low Jack (Garifuna Variations, L.I.E.S, 2014 ; Sewing Machine, In Paradisum, 2015 ; Lighthouse Stories, Modern Love, 2016) and one under his own name, An American Hero (Modern Love, 2017). His live performances include collaborations with musicians Ghedalia Tazartès and Dominick Fernow/Vatican Shadow. His work has been commissionned by the Quai Branly museum, the Swiss Cultural Center and the Ricard Fondation in Paris.
</p>

