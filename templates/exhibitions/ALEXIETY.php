                    <h3>About the exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p>ALEXIETY is panke.gallery Edition #002. Together with French musician
Low Jack, !Mediengruppe Bitnik have been looking at ways to engage with
Alexa and similar <s>Intelligent</s> Personal Assistants through music. A set
of three songs attempt to capture the feelings we develop toward
<s>Intelligent</s> Personal Assistants: The carefree love that embraces Alexa
before the data privacy and surveillance issues outweigh the benefits.
The alienation and decoupling from the allure of remote control and
instant gratification. The anxiety and discomfort around Alexa and other
<s>Intelligent</s> Personal Assistants that is Alexiety.
<br><br>

<s>Intelligent</s> Personal Assistants like Alexa, Google Home and Siri are the
brains of the <s>smart</s> home ecosystem. They operate, monitor and control
<s>smart</s> home appliances while keeping the algorithms and rule-sets that
determine their workings secret. <s>Intelligent</s> Personal Devices are voice
controlled, thus dissolving the machinic presence of the computer while
placing its functionalities at the users disposal. It's like living
inside the machine, while at the same time having no agency over the
composition and structure of ones environment.
<br><br>

What are the relationships that we are forming with these IPA devices?
What happens when IoT devices are hacked to form rogue bot-networks? Is
my capacity to act expanded or diminished when relying on these
semi-autonomous devices?
<br><br>

panke.gallery Edition #002 - Alexiety
EP released on the occasion of Cryptorave with OMSK Social Club and the
birth of <em>RaveEnabler</em> <a href="https://0b673cce.xyz">https://0b673cce.xyz/</a> - mining Monero (XMR) in
support of local Cryptorave networks by donating CPU power.
<br><br>

Multi-platform graphics: Knoth &amp; Renner<br>
