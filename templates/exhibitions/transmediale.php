                    <h3>Participants</h3>
                </div>
                <div class="col-md-8 col-sm-8 col-xs-12">
                    <p><strong>Joachim Blank</strong>, 1963 born in Aachen (DE), is an artist based in Berlin working with different materials, media and formats. In the early 90ies he became a cofounder of early „Net Art“ projects. From 1996 – 2005 he collaborated with Karl Heinz Jeron as Blank & Jeron. Since 2003 he is a Professor of Media Art at the Academy of Fine Arts (HGB) in Leipzig. The confrontation with the digital world has become a main aspect of his own artistic practice. Blanks central question revolves about how media are changing the perception of materiality, how medial arguments are enrolled in the surfaces of the objects and are transforming them. It is about investigating the materiality of things and images, their outer appearance and physical presence in terms of surface-, meaning- and knowledge production. His works were presented in solo and group exhibitions such as MedienBiennale Leipzig (1994), Documenta X (1997), NBK Berlin (1998), ZKM Karlsruhe (1998), Ars Electronica Linz (1999), Institute of Contemporary Art London (2001), Foto Museum Winterthur (2001), National Museum of Contemporary Art Oslo (2003), San Fransisco Museum of Modern Art (2008), TEAF09, Ulsan City, Korea (2009), Berlinische Galerie (2010), National Museum Stettin (2014), Altana Galerie Dresden (2016).</p>
                    <p><strong>Atlas of Media-Thinking and Media-Acting in Berlin</strong> Kristin Moellering, Leon Strauch und <a href=" http://stefanie-rau.com/"> Steffanie Rau</a> </p>
	             	 <p>With its distinctive cultural and historical attributes, the city of Berlin significantly coins the interplay between science, art and technology and thus the high-tension field in which media can probably most likely be localized. The Atlas of MediaThinking and MediaActing in Berlin is an attempt to uncover these diverse origins and to provide an archaeological perspective not only on the ways in which media haven been theorized here since the 1950s, but also on how they have been artistically, socioculturally and technologically negotiated. For this purpose, the map on the front side illuminates relevant places, while about a dozen heterogenous essays on the back side provide additional access levels.  <br>

The Atlas derives from the research project Forum on the Genealogy of MediaThinking, which was initiated by Prof. Dr. Siegfried Zielinski in cooperation with Dr. Eckhard Fürlus at the University of the Arts Berlin. </p>
<p><strong>Dr. Tilman Baumgärtel</strong> is a writer. He lives in Berlin and teaches media studies at Hochschule Mainz. Previously he was a professor at the University of the Philippines in Manila (2005 - 2009) und at the Department of Media and Communication at the Royal University of Phnom Penh (2009 - 2012). He has written or edited nine books on various aspect of media culture, including internet art, computer games, the aesthetics of loops and the director Harun Farocki, including "net.art - Materialien zur Netzkunst" (Nürnberg 1999), "net.art 2.0. Neue Materialien zur Netzkunst: New Materials Towards Net Art" (Nürnberg 2001) and "Schleifen. Zur Geschichte und Ästhetik des Loops" (Berlin 2015).</p>
                    <p><strong>Annet Dekker</strong> is an independent researcher/curator. Currently she is Assistant Professor Archival Science at the University of Amsterdam and Visiting Lecturer at London South Bank University. Previously she was Researcher Digital Preservation at Tate, London, tutor at Piet Zwart Institute, Rotterdam, and fellow at Het Nieuwe Instituut, Rotterdam. She publishes widely on issues of digital art and preservation in international, peer-reviewed journals, books, and magazines, and has edited several publications, among others, Speculative Scenarios, or what will happen to digital art in the (near) future (2013), Archive 2020: Sustainable Archiving of Born Digital Cultural Content (2010), and Walled Garden (co-edited with Annette Wolfsberger, 2009). She worked as web curator for SKOR (2010–12), was programme manager at Virtueel Platform (2008–10), and head of exhibitions, education and artists-in-residence at the Netherlands Media Art Institute (1999–2008). In 2014, she completed her PhD on a conservation of net art at Goldsmiths University of London.</p>
                    <p><strong>Valie Djordievic</strong> lives and works in Berlin. She is an editor at iRights.info, a website about copyright in the digital age, and a lecturer on digital writing, social media and copyright for private and public institutions.<br>
She studied comparative literature, Slavic languages and film studies at the Freie Universität Berlin. She completed a BA (Hons) in Humanities and English Language (upper second) and has a Diploma of Creative Writing (first) from the Open University in the UK .<br>
She is active in the digital world from 1996. She was a member of Internationale Stadt Berlin, one of the first net culture projects in Germany. Since then she worked in different art and cultural projects including mikro e.V., a Berlin based association examining the different facets of media culture, the cyberfeminist group Old Boys Network or the Media Arts Lab at Künstlerhaus Bethanien. 2008 she worked with her collegues from iRights.info on the research project “Arbeit 2.0” about the work conditions of digital creatives at the Humboldt University in Berlin; from 2009 to 2011 she was part of the team of IUWIS, a copyright infrastructure for research and education also based at the Humboldt university. She is a co-moderator and administrator (together with Diana McCarty, Kathy Rae Huffman and Ushi Reiter) of the mailing list Faces, one of the first lists for women working with art and media.</p>
                    <p><strong>Holger Friese</strong> (*1968, lives and works in Bernau bei Berlin) is a visual artist and graphic designer. In the mid to late nineties, he gained attention with his Internet-based artworks and developed and exhibited his work at documenta X, SFMOMA, and ZKM in Karlsruhe among others. In both his artistic and professional work he is interested in collaborating with other experts to expand ideas further than is possible alone. In addition to his everyday work, he has taught media design and media art at various institutions and has lectured in Germany, Europe, and North America. Since 2015 he acts as the artistic director for the international summer school at the Bauhaus Denkmal Bundeschule Bernau..</p>
                    <p><strong>Karl Heinz Jeron</strong> was born in Memmingen, Germany in 1962. He lives and works in Berlin since 1987. From 1993 til 2004 he collaborated with Joachim Blank. His art deals with every day culture, media perception and information processing. He rather creates experiences than objects. Jeron's works span from the singing and dancing robot Sim Gishel, to compositions for electrified rotten vegetables and audio tour guides in onshore wind farms. Recent exhibitions, events and collaborations include ZKM Karlsruhe, Ars Electronica Linz, Documenta X, ICA London, Walker Art Museum Minneapolis, Berlinischen Galerie Berlin and the Museum of Modern Art San Francisco.</p>
                    <p><strong>Pit Schultz</strong> (born 1965) is an author, artist, computer expert and net activist. He lives and works in Berlin. Together with Geert Lovink he founded the genre of net criticism and published various articles. Schultz also co-founded the mailing list nettime.org and the technology lab bootlab. For many years, he has been a member of mikro e.V. and runs several net-based radio stations and co-founded reboot.fm in 2004.
 </p>
                    <p><strong>Cornelia Sollfrank</strong> (PhD) is an artist, researcher and university lecturer, living in Berlin (Germany). He has studied painting at the Academy of Art in Munich and Fine Art at the University of the Arts Hamburg, and got her PhD from University of Dundee (UK).<br>
Her means of expression include writing, performance, sound, video and (other) Internet-based formats. Recurring subjects in her artistic and academic work about digital cultures are authorship, self-organization, gender and techno-feminism. As a pioneer of Internet art, Cornelia Sollfrank built up a reputation with two central projects: the net.art generator – a web-based art-producing ‘machine,’ and Female Extension – her famous hack of the first competition for Internet art. Her experiments with the basic principles of aesthetic modernism implied conflicts with its institutional and legal framework and led to her academic research. In her PhD “Performing the Paradoxes of Intellectual Property,” Cornelia investigated the increasingly conflicting relationship between art and copyright. The current artistic research project Giving What You Don’t Have brings together art projects that all contribute to the creating and maintenance of ‘digital commons.’ Her most recent performance À la recherche de l’information perdue is about gender stereotypes in the digital underground.</p>

