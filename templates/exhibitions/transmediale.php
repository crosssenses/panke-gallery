                    <h3>Exhibition</h3>
                </div>
                <div class="col-md-8 col-sm-8            col-xs-12">
                    <p>&mdash; Kick off zur kritischen Bestandsaufnahme der Netzkunst-Scene der letzten 25 Jahre in Berlin </p>
                    <p>Das Berlin der neunziger Jahre des Letzten Jahrhundert war eines der Zentren der Netzkunst. In den Möglichkeitsräumen der wiedervereinigten Stadt entstanden aus den direkten (personell als auch räumlich) Verbindungen von Club und Lab in techno(logischen) und künstlerischen Experimenten NetzKunstAktivitäten. Die innovative Kraft der frühen NetzKunstAktivitäten, von denen manche noch vor den graphischen Browser Oberflächen entstanden, wird in Fachtexten zwar erwähnt, doch kann sie einer neuen Generation von Netzkünstlern nur noch schwer vermittelt werden, denn teilweise sind sie verschollen oder gänzlich unbrauchbar geworden. Bevor es vollkommen unmöglich ist soll die Ausstellung zum Anlass genommen werden einerseits eine Bestandsaufnahme, mit dem Versuch einer exemplarischen Dokumentation und einer exemplarischen Rekonstruktion als auch der Erstellung von soziogrammatischen Videointerviews mit Protagonisten der Zeit erste Schritte hin zu einer Sicherung zu unternehmen. Wobei die Ausstellung insbesondere durch die <em>performative sociogrammatic</em> der Videointerviews die einzelnen individuellen Perspektiven auf den historischen Zeitabschnitt illustrieren und visualisieren wird.</p>
                    <p>&mdash; 1990s Berlin Net Art &mdash; Towards a Critical Evaluation</p>
                    <p>At February 2017, panke.gallery begins a critical evaluation of 1990s Berlin net art. Numerous early net art activities originated before the invention of graphic browser interfaces and mediating their innovative power has become increasingly difficult. Historical revision of the genre is lacking and many works have either disappeared from the Internet or become useless. At panke.gallery this project initiates a series of exhibitions, talks, and lectures, taking first steps towards an evaluation and contextualization of 90s net art works for the long term.</p>
